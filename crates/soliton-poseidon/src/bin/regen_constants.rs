//! Regenerate `src/constants.rs` (the baked LE byte tables) from the circom-BN254
//! parameters in `soliton_poseidon::gen` (pulled from `light-poseidon`).
//!
//! Run with:
//!   cargo run -p soliton-poseidon --features regen --bin regen-constants > crates/soliton-poseidon/src/constants.rs

use soliton_poseidon::gen::{fr_to_le, gen_mds, gen_round_constants};

fn fmt_le(b: &[u8; 32]) -> String {
    let mut s = String::from("[");
    for (i, x) in b.iter().enumerate() {
        if i > 0 {
            s.push(',');
        }
        s.push_str(&x.to_string());
    }
    s.push(']');
    s
}

fn main() {
    let rc = gen_round_constants();
    let mds = gen_mds();

    println!("//! AUTO-GENERATED by `regen-constants` — DO NOT EDIT BY HAND.");
    println!("//!");
    println!("//! Baked little-endian byte tables for the SOLITON shared Poseidon");
    println!("//! (circom-BN254: width-3, x^5, R_F=8, R_P=57). Pulled verbatim from");
    println!("//! `light-poseidon`'s bn254_x5 parameter set — the SAME constants the");
    println!("//! `sol_poseidon` syscall uses. A host test (`hash2_equals_light_poseidon`)");
    println!("//! asserts the permutation built on these matches light-poseidon byte-for-byte.");
    println!();
    println!("/// Round constants `ark`, reshaped to ROUNDS (=65) rows of T (=3) lanes.");
    println!("pub static ARK_LE: [[[u8; 32]; 3]; 65] = [");
    for row in rc.iter() {
        print!("    [");
        for (j, e) in row.iter().enumerate() {
            if j > 0 {
                print!(", ");
            }
            print!("{}", fmt_le(&fr_to_le(e)));
        }
        println!("],");
    }
    println!("];");
    println!();
    println!("/// circom-BN254 width-3 MDS matrix (3x3).");
    println!("pub static MDS_LE: [[[u8; 32]; 3]; 3] = [");
    for row in mds.iter() {
        print!("    [");
        for (j, e) in row.iter().enumerate() {
            if j > 0 {
                print!(", ");
            }
            print!("{}", fmt_le(&fr_to_le(e)));
        }
        println!("],");
    }
    println!("];");
}
